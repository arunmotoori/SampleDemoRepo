package operatorspack;

public class LiteralsDemo {

	public static void main(String[] args) {
		
		byte a = 127; // Integer literal assigned to byte declared variable
		short b = 9999; // Integer literal assigned to short declared variable
		int c = 1234567891; // Integer literal assigned to int declared variable
		long d = 12345678912L; // Long literal assigned to long declared variable
		float e = 12F; // Floating Point literal assigned to float declared variable
		double f = 123D; // Double literal assigned to double declared variable
		char g = 's'; // Character literal assigned to char declared variable
		boolean h = true; //boolean literal
		String str = "Arun Motoori"; //string literal
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		
	}

}

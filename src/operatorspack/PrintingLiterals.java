package operatorspack;

public class PrintingLiterals {

	public static void main(String[] args) {
		
		System.out.println("Arun Motoori"); // String literal
		System.out.println(99); // Integer literal
		System.out.println(1.23F); // Floating point literal
		System.out.println(2.34D); // Double literal
		System.out.println(12.34); // Double literal
		System.out.println(12.15d); // Double literal
		System.out.println(999999); // Integer Literal
		System.out.println(99999999999L); // Long literal
		System.out.println(99L); // Long literal
		System.out.println(9l); // Long literal
		System.out.println(true); // boolean literal
		System.out.println(false); // boolean literal
		System.out.println('s'); // Character literal
		System.out.println('$'); // Character literal
		System.out.println('9'); // Character literal
		System.out.println("s"); // String literal
		
	}

}
